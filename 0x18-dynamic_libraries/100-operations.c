/**
 * sub - minus
 * add - addition
 * mul - multiply
 * div - divide
 * mod - module
 * @k: integer
 * @m: integer
 *
 * Return:result
 */
int sub(int k, int m)
{
        return (k - m);
}
int add(int k, int m)
{
        return (k + m);
}
int mul(int k, int m)
{
        return (k * m);
}       
int div(int k, int m)
{       
        return (k / m);
}       
int mod(int k, int m)
{
        return (k % m);
}       
